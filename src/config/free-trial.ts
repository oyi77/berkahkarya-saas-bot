export interface UserFreeTrial {
  welcomeBonusUsed: boolean;
  dailyFreeUsed: boolean;
  dailyFreeResetAt: Date | null;
  selectedNiche: string | null;
}

export function canUseWelcomeBonus(user: any): boolean {
  return !user.welcomeBonusUsed;
}

export function canUseDailyFree(user: any): boolean {
  if (!FREE_TRIAL_CONFIG.dailyFree.enabled) return false;
  // If no reset time is set but daily free was already used, deny access
  if (!user.dailyFreeResetAt) return !user.dailyFreeUsed;

  const now = new Date();
  const resetAt = new Date(user.dailyFreeResetAt);
  return now >= resetAt;
}

export async function getFreeTrialConfigAsync(): Promise<typeof FREE_TRIAL_CONFIG> {
  try {
    const { prisma } = await import('./database.js');
    const dbConfig = await prisma.pricingConfig.findUnique({
      where: { category_key: { category: 'free_trial', key: 'config' } },
    });
    if (dbConfig?.value) {
      const val = typeof dbConfig.value === 'string' ? JSON.parse(dbConfig.value) : dbConfig.value;
      return { ...FREE_TRIAL_CONFIG, ...val };
    }
  } catch {}
  return FREE_TRIAL_CONFIG;
}

export function getNextDailyFreeReset(): Date {
  // Calculate "tomorrow midnight" in WIB (UTC+7), then convert back to UTC
  const WIB_OFFSET_MS = 7 * 60 * 60 * 1000;
  const nowWib = new Date(Date.now() + WIB_OFFSET_MS);
  const tomorrowWib = new Date(nowWib);
  tomorrowWib.setUTCDate(tomorrowWib.getUTCDate() + 1);
  tomorrowWib.setUTCHours(0, 0, 0, 0);
  return new Date(tomorrowWib.getTime() - WIB_OFFSET_MS);
}
